package com.ebon.rpc.task.vo;

import java.io.Serializable;

public class TaskResponse implements Serializable {

	private static final long serialVersionUID = 8110275798200870659L;

	private String isOk; // 是否成功
	private String errorCode; // 错误代码
	private String errorMessage; // 错误信息
	private TaskInfo[] taskInfos; // 任务信息

	public String getIsOk() {
		return isOk;
	}

	public void setIsOk(String isOk) {
		this.isOk = isOk;
	}

	public String getErrorCode() {
		return errorCode;
	}

	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}

	public String getErrorMessage() {
		return errorMessage;
	}

	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}

	public TaskInfo[] getTaskInfos() {
		return taskInfos;
	}

	public void setTaskInfos(TaskInfo[] taskInfos) {
		this.taskInfos = taskInfos;
	}
}

package com.ebon.v2.eai.budget.model;

public class Budget {
	String proj_code;
	String PEO;
	String MAD;
	String SER;
	//定义一个保存实际值的字段,是三项费用的总和
	String actualTotal;
	
	public String getActualTotal() {
		return actualTotal;
	}
	public void setActualTotal(String actualTotal) {
		this.actualTotal = actualTotal;
	}
	public String getProj_code() {
		return proj_code;
	}
	public void setProj_code(String proj_code) {
		this.proj_code = proj_code;
	}
	public String getPEO() {
		return PEO;
	}
	public void setPEO(String pEO) {
		PEO = pEO;
	}
	public String getMAD() {
		return MAD;
	}
	public void setMAD(String mAD) {
		MAD = mAD;
	}
	public String getSER() {
		return SER;
	}
	public void setSER(String sER) {
		SER = sER;
	}
}

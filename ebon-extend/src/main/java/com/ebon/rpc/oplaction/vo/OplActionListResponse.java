package com.ebon.rpc.oplaction.vo;

import java.io.Serializable;

public class OplActionListResponse implements Serializable {

	private static final long serialVersionUID = 8927147342350700217L;

	private String isOk;// 是否成功标识
	private String errorCode;// 错误代码
	private String errorMessage;// 错误信息
	private OplActionInfo[] OplActionInfos;// OPL信息
	
	public String getIsOk() {
		return isOk;
	}
	public void setIsOk(String isOk) {
		this.isOk = isOk;
	}
	public String getErrorCode() {
		return errorCode;
	}
	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}
	public String getErrorMessage() {
		return errorMessage;
	}
	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
	public OplActionInfo[] getOplActionInfos() {
		return OplActionInfos;
	}
	public void setOplActionInfos(OplActionInfo[] oplActionInfos) {
		OplActionInfos = oplActionInfos;
	}

}
